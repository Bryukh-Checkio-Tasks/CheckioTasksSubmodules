from random import randint


def route(data, pos=0):
    if len(data) == 1:
        return [data[0][pos], [pos]]
    a = route(data[1:], pos)
    b = route(data[1:], pos + 1)
    m = a if a[0] > b[0] else b
    return [data[0][pos] + m[0], m[1] + [pos]]


